#include "libft.h"

int	ft_count_words(char const *s, char c)
{
	size_t	i;
	size_t	count;

	i = 0;
	count = 0;
	while (s[i])
	{
		if (s[i] != c)
		{
			count++;
			while (s[i] && s[i] != c)
				i++;
		}
		else
			i++;
	}
	return (count);
}

char	**ft_free_split(char **split, size_t j)
{
	while (j--)
		free(split[j]);
	free(split);
	return (NULL);
}

char	**ft_create_split(char const *s, char c, char **split)
{
	size_t	i;
	size_t	j;
	size_t	k;

	i = 0;
	j = 0;
	while (s[i])
	{
		if (s[i] != c)
		{
			k = 0;
			while (s[i + k] && s[i + k] != c)
				k++;
			split[j] = ft_substr(s, i, k);
			if (!split[j])
				return (ft_free_split(split, j));
			j++;
			i += k;
		}
		else
			i++;
	}
	split[j] = 0;
	return (split);
}

char	**ft_split(char const *s, char c)
{
	char	**split;

	if (!s)
		return (NULL);
	split = (char **)malloc((ft_count_words(s, c) + 1) * sizeof(char *));
	if (!split)
		return (NULL);
	split = ft_create_split(s, c, split);
	if (!split)
		return (NULL);
	return (split);
}
